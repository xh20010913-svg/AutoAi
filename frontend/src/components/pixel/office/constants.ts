// Chair positions (row, col) - where agents sit at desks
export const CHAIR_POSITIONS: Array<{ row: number; col: number }> = []
for (let r = 0; r < ROWS; r++) {
  for (let c = 0; c < COLS; c++) {
    if (OFFICE_MAP[r][c] === TileType.CHAIR) {
      CHAIR_POSITIONS.push({ row: r, col: c })
    }
  }
}

// Sofa positions for rest area
export const SOFA_POSITIONS: Array<{ row: number; col: number }> = []
for (let r = 0; r < ROWS; r++) {
  for (let c = 0; c < COLS; c++) {
    if (OFFICE_MAP[r][c] === TileType.SOFA) {
      SOFA_POSITIONS.push({ row: r, col: c })
    }
  }
}

// Walkable floor positions (for pathfinding)
export function isWalkable(row: number, col: number): boolean {
  if (row < 0 || row >= ROWS || col < 0 || col >= COLS) return false
  const t = OFFICE_MAP[row][col]
  return t === TileType.FLOOR || t === TileType.RUG
}
